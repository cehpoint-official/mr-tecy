import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const badgeVariants = cva(
    "inline-flex items-center rounded-full border px-3 py-1 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-offset-2",
    {
        variants: {
            variant: {
                default:
                    "border-transparent bg-[#2563eb] text-white shadow hover:bg-[#1e40af]",
                secondary:
                    "border-transparent bg-slate-100 text-slate-900 hover:bg-slate-200",
                success:
                    "border-transparent bg-[#10b981] text-white shadow",
                warning:
                    "border-transparent bg-[#f59e0b] text-white shadow",
                outline: "text-slate-700 border-slate-300",
            },
        },
        defaultVariants: {
            variant: "default",
        },
    }
)

export interface BadgeProps
    extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> { }

function Badge({ className, variant, ...props }: BadgeProps) {
    return (
        <div className={cn(badgeVariants({ variant }), className)} {...props} />
    )
}

export { Badge, badgeVariants }
